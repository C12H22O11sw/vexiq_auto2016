static void resetDirection(void)
{
	//resets your timer and gyro
	resetGyro(GYRO);
	resetTimer(T4);
}
static float getDirection(void)
{
	//subtract "drift" from whatever degrees you have on the gyro and multiply that
	//by however many seconds T4 reads
	const float drift=0.135; /* deg/sec gyro drift */
	return getGyroHeadingFloat(GYRO) - drift * getTimer(T4, seconds);
}
static void goForwardGyro(const float deg, const int ms)
{
	const int speed = 129;
	const int correction = -5;
	int speedRight = speed;
	int speedLeft = speed;
	float direction;
	setMotorSpeed(L_MOT, speedLeft);
	setMotorSpeed(R_MOT, speedRight);
	setMotorSpeed(L_TMOT, speedLeft);
	setMotorSpeed(R_TMOT, speedRight);
	resetTimer(T1);
	while(getTimer(T1, milliseconds) < ms)
	{
		direction = getDirection();
		if (direction == deg) {
			if (speedRight != speed) {
				speedRight = speed;
				setMotorSpeed(R_MOT, speedRight);
				setMotorSpeed(R_TMOT, speedRight);
			}
			if (speedLeft != speed) {
				speedLeft = speed;
				setMotorSpeed(L_MOT, speedLeft);
				setMotorSpeed(L_TMOT, speedLeft);
			}
		} else if (direction < deg) {
			if (speedRight == speed) {
				speedRight = speed - correction;
				setMotorSpeed(R_MOT, speedRight);
				setMotorSpeed(R_TMOT, speedRight);
			}
			if (speedLeft < speed) {
				speedLeft = speed;
				setMotorSpeed(L_MOT, speedLeft);
				setMotorSpeed(L_TMOT, speedLeft);
			}
		} else {
			if (speedRight < speed) {
				speedRight = speed;
				setMotorSpeed(R_MOT, speedRight);
				setMotorSpeed(R_TMOT, speedRight);
			}
			if (speedLeft == speed) {
				speedLeft = speed - correction;
				setMotorSpeed(L_MOT, speedLeft);
				setMotorSpeed(L_TMOT, speedLeft);
			}
		}
	}
}
